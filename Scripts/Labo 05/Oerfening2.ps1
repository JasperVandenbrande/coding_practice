#Wijs de waarde 12 toe aan variabele $toewijzing 
$toewijzing=12
Write-Output "$toewijzing"
#1. Verhoog de waarde van $toewijzing met 5
$toewijzing +=5
Write-Output "$toewijzing"
#2. Verlaag de waarde van $toewijzing met 3 
$toewijzing -=3
Write-Output "$toewijzing"
#3. Deel de waarde van $toewijzing door 2 
$toewijzing /=2
Write-Output "$toewijzing"
#4. Bereken de restwaarde van $toewijzing met modulus 4 $toewijzing %=4
$toewijzing %=4
Write-Output "$toewijzing"
#5. Verhoog de waarde van $toewijzing met 1 (gebruik hiervoor de increment operator) 
$toewijzing++
Write-Output "$toewijzing"
#6. Verminder de waarde van $toewijzing met 1 (gebruik hiervoor de decrement operator) 
$toewijzing--
Write-Output "$toewijzing"